target("mytest", function()
    set_kind("static")
    add_packages("spdlog", "boost", "cli11")
    add_includedirs("src/mytest/inc")
    add_headerfiles("src/mytest/inc/**.hpp")
    add_files("src/mytest/**.cpp")
    after_build(function(target)
        targetfile = target:targetfile()
        os.cp(targetfile, "./tools/lib/")
    end)
end)

